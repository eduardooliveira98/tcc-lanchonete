    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        ProdutosDAO cDAO = new ProdutosDAO();
        Produto produto = new Produto();
        
        int linhaSelecionada = jTable2.getSelectedRow();
        int codigo = Integer.parseInt(jTable2.getModel().getValueAt(linhaSelecionada, 0).toString());
        
        produto.setIditem(codigo);
        cDAO.excluir(produto);
        
        jButton2.setEnabled(false);
        jButton4.setEnabled(false);
        ResultSet rs= cDAO.listarProdutos();
        carregarDadosNaJTable(rs);
    }//GEN-LAST:event_jButton4ActionPerformed

