    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            ProdutosDAO pDao = new ProdutosDAO();
            int linhaSelecionada=jTable2.getSelectedRow();
            int a=Integer.parseInt(jTable2.getModel().getValueAt(linhaSelecionada, 0).toString());
            ResultSet rs = pDao.consultarProdutos(a);
            int id = 0 ;
                String nome = null;
                float preco = 0;
                String tipo = null;
            while (rs.next()){
                 id = rs.getInt("iditem");
                 nome = rs.getString("nomeitem");
                 preco = rs.getFloat("preco");
                 tipo = rs.getString("tipo");
                 }
                AlterarProduto telaA = new AlterarProduto();
                telaA.setVisible(true); telaA.setResizable(false);
                telaA.recebeProduto(id, nome, tipo, preco);
                
               
           } catch (SQLException ex) {
            Logger.getLogger(ListarProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
        jButton2.setEnabled(false);
        jButton4.setEnabled(false);
    }//GEN-LAST:event_jButton2ActionPerformed

