public void inicializa(ResultSet rs){
        try {
            while(rs.next()){
                jTextField1.setText(String.valueOf(rs.getInt("idfuncionario")));
                jTextField2.setText(String.valueOf(rs.getInt("idcomanda")));
                jTextField5.setText(String.valueOf(rs.getFloat("valortotal")));
                jComboBox4.setSelectedIndex(rs.getInt("mesa")-1);
                if(rs.getBoolean("status")==true)
                jComboBox1.setSelectedIndex(0);
                else jComboBox1.setSelectedIndex(1);
                jDateChooser1.setDate(rs.getDate("dtabertura"));
                jDateChooser2.setDate(rs.getDate("dtfechamento"));
            }   } catch (SQLException ex) {
        Logger.getLogger(AlterarComandaView.class.getName()).log(Level.SEVERE, null, ex);}
        ProdutosDAO produtosDao = new ProdutosDAO();
        carregarDadosNaJTable1(produtosDao.listarProdutos());
        ComandaItensDao pDao = new ComandaItensDao();
        ResultSet k = pDao.consultarComandasItens(Integer.parseInt(jTextField2.getText()));
    try {
        carregarDadosNaJTable(k);
    } catch (SQLException ex) {
        Logger.getLogger(AlterarComandaView.class.getName()).log(Level.SEVERE, null, ex);
    }
}
